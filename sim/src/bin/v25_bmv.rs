//! v2.5 実験による審判 — 重力誘起もつれ (BMV 実験) の定量
//!
//! QRN (A2/A3: 幾何=もつれ) の最も直接的な検証実験が提案されている
//! (Bose et al. / Marletto–Vedral 2017):
//!   2 つの質量をそれぞれ位置の重ね合わせ (L/R) に置き、重力だけで相互作用させる。
//!   重力が量子的 (もつれを媒介できる) なら、2 質量は絡み合う。
//!   重力が古典場なら (LOCC)、もつれは決して生じない — 白黒のつく実験。
//! 各枝の重力位相 φ_ij = G m² τ / (ħ d_ij) から生成されるもつれ (concurrence) を計算する。

fn main() {
    const G: f64 = 6.674e-11;
    const HBAR: f64 = 1.0546e-34;
    println!("=== v2.5 重力誘起もつれ (BMV): 理論の審判となる実験の定量 ===\n");
    println!("状態: (|L⟩+|R⟩)/√2 ⊗ (|L⟩+|R⟩)/√2 → (1/2)Σ e^{{iφ_ij}}|ij⟩");
    println!("もつれ位相 Δφ = φ_LL + φ_RR - φ_LR - φ_RL,  concurrence C = |sin(Δφ/2)|\n");
    println!("  m [kg]   d [μm]  Δx [μm]  τ [s]   Δφ [rad]   C(もつれ)   判定");
    let cases: [(f64, f64, f64, f64); 5] = [
        (1e-14, 250.0, 50.0, 2.5),
        (1e-14, 250.0, 100.0, 2.5),
        (1e-14, 200.0, 100.0, 2.5),
        (1e-14, 200.0, 100.0, 5.0),
        (1e-15, 200.0, 100.0, 2.5), // 軽い質量では?
    ];
    for &(m, d_um, dx_um, tau) in &cases {
        let d = d_um * 1e-6;
        let dx = dx_um * 1e-6;
        let phi = |dist: f64| G * m * m * tau / (HBAR * dist);
        // 幾何: 質量1 の枝 L/R は x=0/dx, 質量2 の枝 L/R は x=d/d+dx → 距離: LL=d, LR=d+dx, RL=d-dx, RR=d
        let dphi = 2.0 * phi(d) - phi(d + dx) - phi(d - dx);
        let conc = (dphi / 2.0).sin().abs();
        println!(
            "  {:6.0e}  {:5.0}   {:5.0}    {:3.1}   {:+.4}    {:.4}      {}",
            m,
            d_um,
            dx_um,
            tau,
            dphi,
            conc,
            if conc > 0.01 { "測定可能域" } else { "困難" }
        );
    }
    println!("\n  スケーリング: Δφ ≈ -G m² τ Δx²/(ħ d³) — 質量の 2 乗、距離の 3 乗に敏感");
    println!("  必要条件 (Bose et al.): 自由落下 τ~2.5s、分離 >157μm (Casimir 力回避)、");
    println!("  超高真空 ~10⁻¹⁵ Pa、極低温 ~0.15K (デコヒーレンス率 γτ < 1)、");
    println!("  もつれ証人 W = |⟨σx⊗σz⟩+⟨σy⊗σy⟩| > 1 の統計に ~C⁻² ≈ 10³-10⁴ 回の反復。\n");
    println!("  意味: もし重力が古典的な媒介 (LOCC) なら C ≡ 0 が測定される。");
    println!("        QRN (幾何=もつれ) は C > 0 を予言する — 反証可能な白黒テスト。");
    println!("        現在の技術ロードマップ (磁気浮上ダイヤモンド微結晶など) で 2030 年代に実現見込み。");
    println!("\n結論: 統一理論はもはや純粋な形而上学ではない。実験室サイズの審判が日程に載っている。");
}
